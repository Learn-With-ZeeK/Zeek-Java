//Print first 10 odd numbers from 0 using for loop

package com.company;

public class Zeek_23_ForLoop {
	public static void main(String[] args) {
		System.out.println("First 10 odd numbers are:");
		
		//Syntax of for loop: for(initialise variable;set condition;update variable){}
		
		for(int i=0;i<10;i++) {
			System.out.println(2*i+1);	
		}
		
		
		
	}
 }
