package com.company;

public class Zeek_21_WhileLoop {
	public static void main(String[] args) {
		
		int i=100;
		while(i<=200) {
			System.out.println(i);
			i++;
		}
		System.out.println("While loop has finished executing it runs if inside bracket value true");
	}

 }
